<script>
    $(document).ready(function() {
        // Inisialisasi Owl Carousel untuk Home Slider
        var homeSlider = $('.home-box').owlCarousel({
            items: 1, // Hanya 1 slide ditampilkan
            loop: true, // Looping carousel
            margin: 15, // Margin antar item
            nav: false, // Sembunyikan navigasi default
            dots: false, // Sembunyikan dots
            autoplay: true, // Auto play aktif
            autoplayTimeout: 8000, // 8 detik per slide
            autoplayHoverPause: true, // Pause saat hover
            animateOut: 'fadeOut', // Animasi fade out
            animateIn: 'fadeIn', // Animasi fade in
            smartSpeed: 1000, // Kecepatan transisi (ms)
            responsive: {
                0: {
                    items: 1
                },
                768: {
                    items: 1
                },
                1024: {
                    items: 1
                }
            },
            onInitialized: function(event) {
                console.log('Home Slider berhasil diinisialisasi');
                // Play video pertama saat carousel ready
                playCurrentVideo(event);
            },
            onTranslated: function(event) {
                // Stop semua video
                stopAllVideos();
                // Play video yang aktif setelah transisi
                playCurrentVideo(event);
            },
            onChanged: function(event) {
                // Stop semua video saat mulai berubah
                stopAllVideos();
            }
        });

        // Fungsi untuk play video yang sedang aktif
        function playCurrentVideo(event) {
            var activeItem = $('.home-box .owl-item.active');
            var video = activeItem.find('video').get(0);

            if (video) {
                video.currentTime = 0; // Reset ke awal
                video.play().catch(function(error) {
                    console.log('Video autoplay prevented:', error);
                });
            }
        }

        // Fungsi untuk stop semua video
        function stopAllVideos() {
            $('.home-box video').each(function() {
                this.pause();
                this.currentTime = 0;
            });
        }

        // Custom navigation buttons (jika Anda ingin menambahkan)
        $('.owl-prev').click(function() {
            homeSlider.trigger('prev.owl.carousel');
        });

        $('.owl-next').click(function() {
            homeSlider.trigger('next.owl.carousel');
        });

        // Optional: Tambahkan kontrol manual dengan keyboard
        $(document).keydown(function(e) {
            if (e.keyCode == 37) { // Left arrow
                homeSlider.trigger('prev.owl.carousel');
            }
            if (e.keyCode == 39) { // Right arrow
                homeSlider.trigger('next.owl.carousel');
            }
        });

        // Pastikan video dimute untuk autoplay (required by browser)
        $('.home-box video').each(function() {
            this.muted = true;
            this.setAttribute('playsinline', ''); // Untuk iOS
            this.setAttribute('webkit-playsinline', ''); // Untuk iOS lama
        });

        // Handle video ended - pindah ke slide berikutnya
        $('.home-box video').on('ended', function() {
            homeSlider.trigger('next.owl.carousel');
        });
    });
</script>
